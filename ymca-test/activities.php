                <p>Table tennis (also called ping pong) is a sport in which two or four players hit a lightweight ball back and forth using a table tennis bat. The game takes place on a hard table divided by a net. Except for the initial serve, players must allow a ball played toward them only one bounce on their side of the table and must return it so that it bounces on the opposite side. Points are scored when a player fails to return the ball within the rules. Play is fast and demands quick reactions. Spinning the ball alters its trajectory and limits an opponent’s options, giving the hitter a great advantage. When doing so the hitter has a good chance of scoring if the spin is successful.

Table tennis is governed by the worldwide organization International Table Tennis Federation, founded in 1926. ITTF currently includes 220 member associations. The table tennis official rules are specified in the ITTF handbook. Table tennis has been an Olympic sport since 1988, with several event categories.<br>

